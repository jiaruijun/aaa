package com.overcloud.pay.vo;


public class PayInfoVO {
	private String bossPowerKey;
	private String bossProductId;
	private String price;
	private String isCancel;
	private String describe;
	private String customphone;
	public String getBossPowerKey() {
		return bossPowerKey;
	}
	public void setBossPowerKey(String bossPowerKey) {
		this.bossPowerKey = bossPowerKey;
	}
	public String getBossProductId() {
		return bossProductId;
	}
	public void setBossProductId(String bossProductId) {
		this.bossProductId = bossProductId;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getIsCancel() {
		return isCancel;
	}
	public void setIsCancel(String isCancel) {
		this.isCancel = isCancel;
	}
	public String getDescribe() {
		return describe;
	}
	public void setDescribe(String describe) {
		this.describe = describe;
	}
	public String getCustomphone() {
		return customphone;
	}
	public void setCustomphone(String customphone) {
		this.customphone = customphone;
	}
	
}
